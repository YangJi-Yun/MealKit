package com.shop.ex.dto;

public class ProductDTO {
	private String productId;
	private String productName;
	private String productDesc;
	private int cost;
	private int price;
	private int discount;
	private String productContents;
	private String productImage;
	private String productUse;
	private String productReg;
	private int productKind;
	private String productCategory;
	private int count;
	
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductDesc() {
		return productDesc;
	}
	public void setProductDesc(String productDesc) {
		this.productDesc = productDesc;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getDiscount() {
		return discount;
	}
	public void setDiscount(int discount) {
		this.discount = discount;
	}
	public String getProductContents() {
		return productContents;
	}
	public void setProductContents(String productContents) {
		this.productContents = productContents;
	}
	public String getProductImage() {
		return productImage;
	}
	public void setProductImage(String productImage) {
		this.productImage = productImage;
	}
	public String getProductUse() {
		return productUse;
	}
	public void setProductUse(String productUse) {
		this.productUse = productUse;
	}
	public String getProductReg() {
		return productReg;
	}
	public void setProductReg(String productReg) {
		this.productReg = productReg;
	}
	
	public String getProductCategory() {
		return productCategory;
	}
	public void setProductCategory(String productCategory) {
		this.productCategory = productCategory;
	}
	public int getProductKind() {
		return productKind;
	}
	public void setProductKind(int productKind) {
		this.productKind = productKind;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
}
